public class PerimeterOfRectangle {
    public static void main(String[] args) {
        int length = 5;
        int breadth = 4;

        //displaying length and breadth of rectangle
        System.out.println("The length of rectangle is " + length);
        System.out.println("The breadth of rectangle is " + breadth);

        //calculating the perimeter of rectangle
        int perimeter = 2*(length + breadth);

        //displaying the perimeter of rectangle
        System.out.println("The perimeter of rectangle is " + perimeter);
    }
}
