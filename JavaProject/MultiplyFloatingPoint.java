public class MultiplyFloatingPoint {
    public static void main(String[] args) {
        // Define the first floating-point number
        double num1 = 3.5;
        
        // Define the second floating-point number
        double num2 = 2.5;
        
        // Multiply the two numbers
        double result = num1 * num2;
        
        // Display the result
        System.out.println("The result of multiplying " + num1 + " and " + num2 + " is: " + result);
    }
}
